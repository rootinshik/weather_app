export const celsiusToFahrenheit = (c: number): number => c * 9 / 5 + 32;

export const celsiusToKelvin = (c: number): number => c + 273.15;

export const msToKmh = (ms: number): number => ms * 3.6;

export const msToMph = (ms: number): number => ms * 2.23694;

export const hpaToMmhg = (hpa: number): number => hpa * 0.750062;
